                <p>Aedhortensia Polar Bear on ilus ja vastupidav põõsas, mis kuulub hortensiate perekonda. Sellel on suured, valged õied, mis muutuvad õitsemise käigus järk-järgult roosakaks või roheliseks. Polar Bear on eriti hinnatud oma tugevuse ja külmakindluse poolest, taludes ka madalamaid temperatuure. Taim kasvab 1,5–2 meetri kõrguseks ja pakub aedadesse täiendavat ilu suve lõpust kuni sügiseni. Taim armastab päikesepaistelist või poolvarjulist asukohta ja viljakat, hästi kuivendatud mulda. Polar Bear on suurepärane valik, kui otsite aeda, mis õitseb pikka aega ja pakub silmailu ka hilissügisel.</p>
                
