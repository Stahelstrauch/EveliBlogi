<p>ladina keeles: <em>Hosta `Blue Ivory`</em> </p>
<p>Hosta 'Blue Ivory' on kaunis ja atraktiivne hosta sort, millel on eriline lehtede värvus ja mustrid. Sellel on laiad südamekujulised lehed, mille servad on sinakas-hallid, samas kui lehe keskosa on heledam, peaaegu valkjas või kreemikas, luues ilusa kontrasti. See sort on mõõduka suurusega, ulatudes umbes 30–40 cm kõrguseks ja 60 cm laiuseks. Hosta 'Blue Ivory' eelistab osalist varju ja sobib hästi varjulisematesse aedadesse. Suve alguses ilmuvad sellel ka valged või helelillad õied, mis lisavad veel visuaalset huvi. Kokkuvõttes on see suurepärane valik madalate püsilillepeenarde ja maapinna katmiseks. <br>
Hosta 'Blue Ivory' kasvutingimused on järgmised:
<ul>
  <li><strong>Valgus:</strong> See sort eelistab osalist varju või varju, kuigi võib taluda ka kerget päikesevalgust, kuid liigse päikesevalguse korral võivad lehed kahjustuda ja kaotada oma värvi.</li>
  <li><strong>Muld:</strong> Hosta 'Blue Ivory' armastab niisket ja hästi kuivendatud mulda, mis on rikas orgaaniliste ainete poolest. Taime juured vajavad niiskust, kuid oluline on, et muld ei oleks vettinud.</li>
  <li><strong>Temperatuur:</strong> Hosta on külmakindel taim, mis talub hästi külma kliimat. See on talvekindel ja talub talvel külmumist.</li>
  <li><strong>Kastmine:</strong> Hosta 'Blue Ivory' vajab regulaarset kastmist, eriti kuivematel perioodidel. Samas tuleks vältida liigset niiskust, kuna see võib põhjustada juuremädanikku.</li>
  <li><strong>Hooldus:</strong> Kui hosta on rajatud varjulisse kohta, siis vajab see suhteliselt vähe hooldust. Lehtede pruunistumine suve lõpus on normaalne, kuid soovitatav on lõigata ära kuivanud ja kahjustatud lehed, et taim näeks välja korrastatud.</li>
</ul>
