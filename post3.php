<p class="text-center">Ladina keeles: <em>Astilbe x arendsii 'Look at Me'</em></p>
<p class="text-justify">Astilbe *Look at Me* on dekoratiivne ja silmapaistev mitmeaastane taim, mis paistab silma oma suurtelt roosadelt õisikutelt. See sort on kompaktne, kasvades umbes 30–40 cm kõrguseks, ja sobib ideaalselt varjulisematesse aedadesse või niiskematesse kohtadesse. *Look at Me* talub hästi varju ning pakub kauni värvi ja tekstuuri suve lõpus ja varasügisel. Tema õied tõmbavad tähelepanu, samas kui tema roheline lehtkatt on kaunilt kaunistatud ja lisab täiendavat ilu aeda.</p>

<p class="text-justify">Astilbe 'Look at Me' on silmapaistev ja dekoratiivne püsik, mis on tuntud oma suursuguste ja elavate õisikute poolest. Selle taime kirjeldus on järgmine:</p>
<ul>
   <li><strong>Õied:</strong> Astilbe 'Look at Me' õied on erksad, roosad või roosakas-lillad ja ilmuvad suve alguses. Õied on tihedad ja kohevates õisikutes, andes taimele dramaatilise ja tähelepanuväärse välimuse.</li>
   <li><strong>Kasv:</strong> Taim on kompaktne ja ulatub umbes 40–50 cm kõrguseks. Sobib hästi madalate püsilillepeenarde, piiride või varjuliste aedade kaunistamiseks.</li>
   <li><strong>Lehed:</strong> Taimel on läikivad, tumerohelised lehed, mis pakuvad kontrasti õitsvate õisikute taustal.</li>
   <li><strong>Kasvutingimused:</strong> Astilbe 'Look at Me' eelistab varjulisemaid või poolvarjulisi kohti ja niisket, hästi kuivendatud mulda. Taim talub nii jahedamaid kui ka sooje temperatuuride kõikumisi, kuid vajab regulaarset kastmist, et mullas oleks piisavalt niiskust.</li>
   <li><strong>Hooldus:</strong> Astilbe vajab vähe hooldust, kuid aeg-ajalt tuleb eemaldada kuivanud õied ja lehed. Taime saab talveks multšida, et pakkuda lisakaitset külma eest.</li>
</ul>

